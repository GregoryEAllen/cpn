#include "CPNHost.h"

	
//-----------------------------------------------------------------------------
CPNHost::CPNHost(const std::string &myName, const HostID &myID) 
//-----------------------------------------------------------------------------
:	hostName(myName), 
	hostID(myID),
	startNode((myID*NODEBLOCKLEN)+1)
{
	Debug("Constructor called for "+ hostName);
}


//-----------------------------------------------------------------------------
CPNHost::CPNHost(const CPNHost &theCopy)
//-----------------------------------------------------------------------------
:	hostName(theCopy.hostName),
	hostID(theCopy.hostID),
	startNode(theCopy.startNode)
{
	Debug("Copy "+ hostName);
}


//-----------------------------------------------------------------------------
CPNHost::~CPNHost(void) 
//-----------------------------------------------------------------------------
{
	Debug("Destructor called for "+ hostName);
}


//-----------------------------------------------------------------------------
void CPNHost::Print(void)
//-----------------------------------------------------------------------------
{
	printf("name: %s; ID: %d; startNode; %d\n", hostName.c_str(), hostID, startNode); 
}


//-----------------------------------------------------------------------------
void CPNHost::Debug(const std::string &msg)
//-----------------------------------------------------------------------------
{
	if(DEBUG) printf("CPNHost: ### %s\n", msg.c_str());
}
